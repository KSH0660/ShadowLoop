let player;
let playerReady = false;
let state = {
  videoId: null,
  title: "아직 선택된 영상이 없습니다",
  segments: [],
  current: 0,
  repeat: true,
  speed: 1,
};

const RING_CIRCUMFERENCE = 2 * Math.PI * 54;

const els = {
  urlForm: document.querySelector("#urlForm"),
  youtubeUrl: document.querySelector("#youtubeUrl"),
  sampleChips: [...document.querySelectorAll(".sample-chip")],
  emptyPlayer: document.querySelector("#emptyPlayer"),
  segmentList: document.querySelector("#segmentList"),
  segmentCount: document.querySelector("#segmentCount"),
  videoTitle: document.querySelector("#videoTitle"),
  timeRange: document.querySelector("#timeRange"),
  prevSegment: document.querySelector("#prevSegment"),
  nextSegment: document.querySelector("#nextSegment"),
  playPause: document.querySelector("#playPause"),
  repeatSegment: document.querySelector("#repeatSegment"),
  ringProgress: document.querySelector("#ringProgress"),
  speedButtons: [...document.querySelectorAll("[data-speed]")],
};

function cue(text, start, end) {
  return { text: Array.isArray(text) ? text.join("\n") : text, start, end };
}

function onYouTubeIframeAPIReady() {
  player = new YT.Player("player", {
    height: "390",
    width: "640",
    videoId: "",
    playerVars: {
      controls: 1,
      modestbranding: 1,
      rel: 0,
      playsinline: 1,
      cc_load_policy: 1,
      cc_lang_pref: "en",
    },
    events: {
      onReady: () => {
        playerReady = true;
        if (state.videoId) {
          player.loadVideoById({
            videoId: state.videoId,
            startSeconds: state.segments[state.current]?.start || 0,
          });
          player.setPlaybackRate(state.speed);
        }
      },
      onStateChange: handlePlayerState,
    },
  });
}

window.onYouTubeIframeAPIReady = onYouTubeIframeAPIReady;

function parseYouTubeId(value) {
  try {
    const url = new URL(value.trim());
    if (url.hostname.includes("youtu.be")) return url.pathname.slice(1).split("/")[0];
    if (url.searchParams.get("v")) return url.searchParams.get("v");
    const shorts = url.pathname.match(/\/shorts\/([^/?]+)/);
    if (shorts) return shorts[1];
    const embed = url.pathname.match(/\/embed\/([^/?]+)/);
    if (embed) return embed[1];
  } catch {
    return null;
  }
  return null;
}

function loadVideo({ id, title, segments, url, source = "sample" }) {
  state.videoId = id;
  state.title = title || "Custom YouTube video";
  state.segments = segments?.length ? segments : makeFallbackSegments(0);
  state.current = 0;
  state.source = source;
  els.youtubeUrl.value = url || `https://www.youtube.com/watch?v=${id}`;
  els.emptyPlayer.classList.add("is-hidden");

  if (playerReady && player) {
    player.loadVideoById({ videoId: id, startSeconds: state.segments[0]?.start || 0 });
    player.setPlaybackRate(state.speed);
  }

  highlightActiveSample(id);
  render();
}

function makeFallbackSegments(duration) {
  const total = Number.isFinite(duration) && duration > 0 ? duration : 360;
  const chunks = [];
  let start = 0;
  let index = 1;

  while (start < total && chunks.length < 24) {
    const remaining = total - start;
    const length = remaining < 70 ? remaining : 45;
    const end = Math.min(total, start + Math.max(30, length));
    chunks.push({
      start,
      end,
      text: `구간 ${index} · YouTube 플레이어의 자막(CC)을 켜고 이 구간을 반복해 따라 말해보세요.`,
    });
    start = end;
    index += 1;
  }

  return chunks;
}

function handlePlayerState(event) {
  if (event.data === YT.PlayerState.PLAYING) {
    els.playPause.classList.add("is-playing");
    els.playPause.setAttribute("aria-label", "일시정지");
  }

  if (event.data === YT.PlayerState.PAUSED || event.data === YT.PlayerState.ENDED) {
    els.playPause.classList.remove("is-playing");
    els.playPause.setAttribute("aria-label", "재생");
  }
}

function playCurrentSegment() {
  const segment = state.segments[state.current];
  if (!playerReady || !segment) return;
  player.seekTo(segment.start, true);
  player.playVideo();
  player.setPlaybackRate(state.speed);
}

function selectSegment(index, autoplay = true) {
  if (!state.segments.length) return;
  state.current = Math.max(0, Math.min(index, state.segments.length - 1));
  render();
  if (autoplay) playCurrentSegment();
}

function render() {
  const segment = state.segments[state.current];
  els.videoTitle.textContent = state.title;
  els.segmentCount.textContent = `${state.segments.length}개 구간`;
  els.timeRange.textContent = segment ? `${formatTime(segment.start)} / ${formatTime(segment.end)}` : "--:-- / --:--";

  els.repeatSegment.setAttribute("aria-pressed", String(state.repeat));

  els.speedButtons.forEach((button) => {
    button.classList.toggle("is-active", Number(button.dataset.speed) === state.speed);
  });

  els.segmentList.innerHTML = state.segments
    .map((item, index) => `
        <button type="button" class="segment-item ${index === state.current ? "is-active" : ""}" data-index="${index}">
          <span class="segment-index">${String(index + 1).padStart(2, "0")}</span>
          <span class="segment-range">${formatTime(item.start)}–${formatTime(item.end)}</span>
          <span class="segment-dur">${Math.round(item.end - item.start)}초</span>
        </button>
      `)
    .join("");

  updateRing(0);
}

function updateRing(fraction) {
  if (!els.ringProgress) return;
  const clamped = Math.max(0, Math.min(1, fraction));
  els.ringProgress.style.strokeDashoffset = String(RING_CIRCUMFERENCE * (1 - clamped));
}

function tick() {
  if (playerReady && state.segments.length && typeof player.getCurrentTime === "function") {
    const segment = state.segments[state.current];
    const now = player.getCurrentTime();
    const status = player.getPlayerState?.();

    const span = segment.end - segment.start;
    if (span > 0) updateRing((now - segment.start) / span);

    if (status === YT.PlayerState.PLAYING && now >= segment.end - 0.15) {
      if (state.repeat) {
        player.seekTo(segment.start, true);
      } else if (state.current === state.segments.length - 1) {
        player.pauseVideo();
      } else {
        selectSegment(state.current + 1, true);
      }
    }
  }

  requestAnimationFrame(tick);
}

requestAnimationFrame(tick);

function formatTime(seconds) {
  const mins = Math.floor(seconds / 60);
  const secs = Math.floor(seconds % 60);
  return `${String(mins).padStart(2, "0")}:${String(secs).padStart(2, "0")}`;
}

function highlightActiveSample(id) {
  els.sampleChips.forEach((button, index) => {
    button.classList.toggle("is-active", samples[index].id === id);
  });
}

els.urlForm.addEventListener("submit", (event) => {
  event.preventDefault();
  const id = parseYouTubeId(els.youtubeUrl.value);
  if (!id) {
    els.youtubeUrl.setCustomValidity("올바른 YouTube URL을 입력해주세요.");
    els.youtubeUrl.reportValidity();
    return;
  }

  els.youtubeUrl.setCustomValidity("");
  const sample = samples.find((item) => item.id === id);
  if (sample) {
    loadVideo(sample);
    return;
  }

  loadVideo({
    id,
    title: "Custom YouTube video",
    segments: makeFallbackSegments(0),
    url: els.youtubeUrl.value,
    source: "custom",
  });

  setTimeout(() => {
    if (state.source === "custom" && playerReady && player?.getDuration) {
      const duration = player.getDuration();
      state.segments = makeFallbackSegments(duration);
      render();
    }
  }, 1400);
});

els.sampleChips.forEach((button) => {
  button.addEventListener("click", () => {
    loadVideo(samples[Number(button.dataset.sample)]);
  });
});

els.segmentList.addEventListener("click", (event) => {
  const item = event.target.closest(".segment-item");
  if (!item) return;
  selectSegment(Number(item.dataset.index), true);
});

els.prevSegment.addEventListener("click", () => selectSegment(state.current - 1, true));
els.nextSegment.addEventListener("click", () => selectSegment(state.current + 1, true));

els.playPause.addEventListener("click", () => {
  if (!playerReady || !state.segments.length) return;
  const status = player.getPlayerState();
  if (status === YT.PlayerState.PLAYING) {
    player.pauseVideo();
  } else {
    player.playVideo();
    player.setPlaybackRate(state.speed);
  }
});

els.repeatSegment.addEventListener("click", () => {
  state.repeat = !state.repeat;
  render();
});

els.speedButtons.forEach((button) => {
  button.addEventListener("click", () => {
    state.speed = Number(button.dataset.speed);
    if (playerReady && player) player.setPlaybackRate(state.speed);
    render();
  });
});

document.addEventListener("keydown", (event) => {
  if (event.target instanceof HTMLInputElement) return;
  const key = event.key.toLowerCase();
  if (key === " ") {
    event.preventDefault();
    els.playPause.click();
  }
  if (key === "arrowleft") selectSegment(state.current - 1, true);
  if (key === "arrowright") selectSegment(state.current + 1, true);
  if (key === "r") els.repeatSegment.click();
});

